class Path():
    def full(path):
        return os.path.abspath(path)

    def current(self=None):
        return os.path.dirname(os.path.realpath(__file__))

    def extend(*paths):
        for path_ in paths:
            try:
                path = os.path.join(str(path), str(path_))
            except:
                if (get_os() == "windows") and path_ == backslash:  # support for smb windows paths like \\ip_or_pcname\dir\
                    path = backslash * 2
                elif (get_os() == "windows") and (len(path_) <= 3):
                    path = os.path.join(path_, os.sep)
                elif get_os() == "windows":
                    path = path_
                elif (get_os() == "macos") or (get_os() == "linux"):
                    path = os.path.join(os.sep, path_)
        return path

    def home():
        if get_os() == "windows":
            path = Console.get_output(r"echo %userprofile%")
            path = path.rstrip(newline2)
        else:
            path = Console.get_output("echo $HOME", split_lines = True)[0]
            path = path.rstrip(newline)
        return path

